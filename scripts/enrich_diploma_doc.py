def is_heading(text: str) -> bool:
    if text in HEADING_TEXTS:
        return True
    if re.match(r"^\d+(?:\.\d+)*\s+", text):
        return True
    return False


def find_idx(paragraphs, heading: str):
    for i, p in enumerate(paragraphs):
        if p.text.strip() == heading:
            return i
    return None


def next_heading_idx(paragraphs, start_idx: int) -> int:
    for i in range(start_idx + 1, len(paragraphs)):
        if is_heading(paragraphs[i].text.strip()):
            return i
    return len(paragraphs)


def section_contains_marker(paragraphs, start: int, end: int, marker: str) -> bool:
    for i in range(start + 1, end):
        if marker in paragraphs[i].text:
            return True
    return False


def main() -> None:
    doc = docx.Document(DOC_PATH)
    paragraphs = doc.paragraphs

    for heading, lines in EXPANSIONS.items():
        h_idx = find_idx(paragraphs, heading)
        if h_idx is None:
            continue
        end_idx = next_heading_idx(paragraphs, h_idx)
        marker = lines[0][:48]
        if section_contains_marker(paragraphs, h_idx, end_idx, marker):
            continue
        anchor = paragraphs[end_idx] if end_idx < len(paragraphs) else None
        for line in reversed(lines):
            if anchor is None:
                doc.add_paragraph(line)
            else:
                anchor.insert_paragraph_before(line)
        paragraphs = doc.paragraphs

    src_idx = find_idx(paragraphs, "СПИСОК ИСПОЛЬЗОВАННЫХ ИСТОЧНИКОВ И ЛИТЕРАТУРЫ")
    if src_idx is not None:
        end_idx = next_heading_idx(paragraphs, src_idx)
        if not section_contains_marker(paragraphs, src_idx, end_idx, "1. Szeliski R."):
            anchor = paragraphs[end_idx] if end_idx < len(paragraphs) else None
            for line in reversed(SOURCES_BLOCK):
                if anchor is None:
                    doc.add_paragraph(line)
                else:
                    anchor.insert_paragraph_before(line)

    doc.save(DOC_PATH)
    print("Diploma document enriched")


if __name__ == "__main__":
    main()
